package kotprog;

public class Szereplesek {
    private int szineszid;
    private int filmid;
    private String szerep;
    private String szinesznev;
    private String filmcim;

    public Szereplesek() {
    }

    public int getSzineszid() {
        return szineszid;
    }

    public void setSzineszid(int szineszid) {
        this.szineszid = szineszid;
    }

    public int getFilmid() {
        return filmid;
    }

    public void setFilmid(int filmid) {
        this.filmid = filmid;
    }

    public String getSzerep() {
        return szerep;
    }

    public void setSzerep(String szerep) {
        this.szerep = szerep;
    }

    public String getSzinesznev() {
        return szinesznev;
    }

    public void setSzinesznev(String szinesznev) {
        this.szinesznev = szinesznev;
    }

    public String getFilmcim() {
        return filmcim;
    }

    public void setFilmcim(String filmcim) {
        this.filmcim = filmcim;
    }
}
