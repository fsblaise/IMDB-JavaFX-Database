package kotprog;

public class Query {
    private String szinesznev;
    private String mufaj;
    private float ertekeles;
    private int darab;
    private String nev;

    public String getSzinesznev() {
        return szinesznev;
    }

    public void setSzinesznev(String szinesznev) {
        this.szinesznev = szinesznev;
    }

    public String getMufaj() {
        return mufaj;
    }

    public void setMufaj(String mufaj) {
        this.mufaj = mufaj;
    }

    public float getErtekeles() {
        return ertekeles;
    }

    public void setErtekeles(float ertekeles) {
        this.ertekeles = ertekeles;
    }

    public int getDarab() {
        return darab;
    }

    public void setDarab(int darab) {
        this.darab = darab;
    }

    public String getNev() {
        return nev;
    }

    public void setNev(String nev) {
        this.nev = nev;
    }
}
